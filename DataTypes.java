// Program: Demonstrating basic data types

public class DataTypes{

    public static void main(String[] args) {

        // Primitive Data Types

        int age = 20;              // int → whole numbers
        double price = 19.99;      // double → decimal numbers
        char grade = 'A';          // char → single character (use single quotes)
        boolean isJavaFun = true;  // boolean → true or false

        // Non-primitive Data Type
        String name = "John";      // String → text (use double quotes)

        // Printing values
        System.out.println("Name: " + name);
        System.out.println("Age: " + age);
        System.out.println("Price: " + price);
        System.out.println("Grade: " + grade);
        System.out.println("Is Java Fun? " + isJavaFun);
    }
}