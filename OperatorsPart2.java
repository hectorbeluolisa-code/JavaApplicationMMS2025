// Program: Comparison Operators

public class OperatorsPart2{

    public static void main(String[] args) {

        int x = 5;
        int y = 10;

        System.out.println(x == y);  // equal to
        System.out.println(x != y);  // not equal
        System.out.println(x > y);   // greater than
        System.out.println(x < y);   // less than
        System.out.println(x >= y);  // greater or equal
        System.out.println(x <= y);  // less or equal
    }
}